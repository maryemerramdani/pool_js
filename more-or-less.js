function more(Value) {
    return Value+1;
}

function less(Value) {
    return Value-1;
}

function add(a,b) {
    return a+b;
}

function sub(a,b) {
    return a-b;
}
console.log(more(5)); // Should output 6
console.log(less(5)); // Should output 4
console.log(add(5, 3)); // Should output 8
console.log(sub(5, 3)); // Should output 2